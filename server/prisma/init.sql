-- 创建数据库（如已存在可跳过此行）
CREATE DATABASE IF NOT EXISTS `parenting`
  DEFAULT CHARACTER SET utf8mb4
  DEFAULT COLLATE utf8mb4_unicode_ci;

USE `parenting`;

-- ================================================================
-- 1. 用户表
-- ================================================================
DROP TABLE IF EXISTS `users`;
CREATE TABLE `users` (
  `id`         INT          NOT NULL AUTO_INCREMENT,
  `openid`     VARCHAR(64)  NOT NULL,
  `union_id`   VARCHAR(64)  DEFAULT NULL,
  `nickname`   VARCHAR(64)  NOT NULL DEFAULT '',
  `avatar_url` VARCHAR(512) NOT NULL DEFAULT '',
  `phone`      VARCHAR(20)  DEFAULT NULL,
  `role`       ENUM('mother','father','grandparent','other') NOT NULL DEFAULT 'mother',
  `created_at` DATETIME(3)  NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  `updated_at` DATETIME(3)  NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
  PRIMARY KEY (`id`),
  UNIQUE KEY `users_openid_key` (`openid`),
  KEY `users_openid_idx` (`openid`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ================================================================
-- 2. 宝宝档案表
-- ================================================================
DROP TABLE IF EXISTS `babies`;
CREATE TABLE `babies` (
  `id`           INT         NOT NULL AUTO_INCREMENT,
  `user_id`      INT         NOT NULL,
  `nickname`     VARCHAR(64) NOT NULL,
  `gender`       ENUM('male','female') NOT NULL,
  `birthday`     DATE        NOT NULL,
  `avatar_url`   VARCHAR(512) NOT NULL DEFAULT '',
  `feeding_type` ENUM('breast','formula','mixed') NOT NULL DEFAULT 'breast',
  `allergies`    JSON        DEFAULT NULL,
  `blood_type`   VARCHAR(8)  DEFAULT NULL,
  `created_at`   DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  `updated_at`   DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
  PRIMARY KEY (`id`),
  KEY `babies_user_id_idx` (`user_id`),
  CONSTRAINT `babies_user_id_fkey` FOREIGN KEY (`user_id`) REFERENCES `users` (`id`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ================================================================
-- 3. 成长记录表
-- ================================================================
DROP TABLE IF EXISTS `growth_records`;
CREATE TABLE `growth_records` (
  `id`          INT         NOT NULL AUTO_INCREMENT,
  `baby_id`     INT         NOT NULL,
  `user_id`     INT         NOT NULL,
  `type`        ENUM('growth','feeding','sleep','milestone','photo','health','note') NOT NULL,
  `record_date` DATE        NOT NULL,
  `age_month`   TINYINT     NOT NULL DEFAULT 0,
  `age_day`     SMALLINT    NOT NULL DEFAULT 0,
  `data`        JSON        NOT NULL,
  `note`        TEXT        DEFAULT NULL,
  `images`      JSON        DEFAULT NULL,
  `tags`        JSON        DEFAULT NULL,
  `created_at`  DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  `updated_at`  DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
  PRIMARY KEY (`id`),
  KEY `growth_records_baby_id_record_date_idx` (`baby_id`, `record_date`),
  KEY `growth_records_baby_id_type_idx` (`baby_id`, `type`),
  CONSTRAINT `growth_records_baby_id_fkey` FOREIGN KEY (`baby_id`) REFERENCES `babies` (`id`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ================================================================
-- 4. AI 对话会话表
-- ================================================================
DROP TABLE IF EXISTS `conversations`;
CREATE TABLE `conversations` (
  `id`         INT          NOT NULL AUTO_INCREMENT,
  `user_id`    INT          NOT NULL,
  `baby_id`    INT          NOT NULL,
  `title`      VARCHAR(128) NOT NULL DEFAULT '',
  `created_at` DATETIME(3)  NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  `updated_at` DATETIME(3)  NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
  PRIMARY KEY (`id`),
  KEY `conversations_user_id_baby_id_idx` (`user_id`, `baby_id`),
  CONSTRAINT `conversations_user_id_fkey` FOREIGN KEY (`user_id`) REFERENCES `users` (`id`) ON DELETE CASCADE ON UPDATE CASCADE,
  CONSTRAINT `conversations_baby_id_fkey` FOREIGN KEY (`baby_id`) REFERENCES `babies` (`id`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ================================================================
-- 5. AI 对话消息表
-- ================================================================
DROP TABLE IF EXISTS `conversation_messages`;
CREATE TABLE `conversation_messages` (
  `id`              INT          NOT NULL AUTO_INCREMENT,
  `conversation_id` INT          NOT NULL,
  `role`            ENUM('user','assistant','system') NOT NULL,
  `content`         TEXT         NOT NULL,
  `tokens_used`     INT          NOT NULL DEFAULT 0,
  `created_at`      DATETIME(3)  NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  PRIMARY KEY (`id`),
  KEY `conversation_messages_conversation_id_idx` (`conversation_id`),
  CONSTRAINT `conversation_messages_conversation_id_fkey` FOREIGN KEY (`conversation_id`) REFERENCES `conversations` (`id`) ON DELETE CASCADE ON UPDATE CASCADE
